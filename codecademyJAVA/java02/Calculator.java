public class Calculator {
    //加
    public static int getPlus(int a,int b){
        return a + b;
    }

    //减
    public static int getSubtract(int a,int b){
        return a - b;
    }


    //乘
    public static int getMultiplication(int a,int b){
        return a * b;
    }

    //乘
    public static int getDivide(int a,int b){
        return a / b;
    }



}
